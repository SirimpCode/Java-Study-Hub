package my.day02;

/**타입별 초기화 테스트 클래스*/
public class TypeInitialization {
	private char character;
	private int integer;
	private double doubles;
	private String string;
	
	public String getString() {
		return this.string;
	}
	public char getChar() {
		return this.character;
	}
	public int getInteger() {
		return this.integer;
	}
	public double getdouble() {
		return this.doubles;
	}
	
}
